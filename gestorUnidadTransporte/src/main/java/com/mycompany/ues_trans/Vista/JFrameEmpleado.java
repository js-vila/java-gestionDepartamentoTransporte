    //Boton Editar
    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
      
        //Validamos que el usuario hay selccionado un registro para editarlo
        if (idSeleccionado == -1) {
        JOptionPane.showMessageDialog(this, "Por favor, seleccione un empleado de la tabla primero.");
        return;
    }
        if (txtNombre.getText().trim().isEmpty() || txtSalario.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, no deje campos vacíos.", "Campos incompletos", javax.swing.JOptionPane.WARNING_MESSAGE); //Validamos que no haya campos vacios
        return; 
    }

    double salarioNuevo = 0;
    try {
        salarioNuevo = Double.parseDouble(txtSalario.getText().trim());
        if (salarioNuevo <= 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "El salario debe ser mayor a cero.", "Dato inválido", javax.swing.JOptionPane.ERROR_MESSAGE); //Valida que sea un numero y mayor que cero 
            return; 
        }
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "El salario debe ser un número válido, sin letras.", "Error de formato", javax.swing.JOptionPane.ERROR_MESSAGE);
        return; //Valida que sean numeros
    }

    //Capturamos los datos que están en las cajas de texto (por si el usuario los modificó)
    String nombreNuevo = txtNombre.getText();
    String departamentoNuevo = cmbDepartamento.getSelectedItem().toString();
  
    Empleado empleado = new Empleado(nombreNuevo, departamentoNuevo, salarioNuevo);
    
    //Le mandamos el ID que guardamos en la variable global
    empleado.setId(idSeleccionado); 

    //Volvemos a aplicar la SOBRECARGA para recalcular el bono
    if (departamentoNuevo.equals("Administrativo")) {
        empleado.CalcularBono(salarioNuevo); 
    } else if (departamentoNuevo.equals("Motorista")) {
        empleado.CalcularBono(salarioNuevo, 30.0); 
    }
    
    //Mandamos a actualizar a la BD
    //Actualizamos el registro seleccionado por el usuario
    //A traves del metodo actuzlizar de nuestra clase EmpleadoModel
    EmpleadoModel empModel = new EmpleadoModel();
    if (empModel.actualizar(empleado)) {
        JOptionPane.showMessageDialog(this, "Empleado actualizado con éxito.");
        
        //Recargamos la tabla 
        cargarDatosEnTabla(); 
        
        //Reiniciamos la variable global que nos ayuda a selccionar al usuario
        idSeleccionado = -1; 
        
        //Limpiamos los campos
        txtNombre.setText("");
        txtSalario.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Error al actualizar el empleado.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    //Boton Eliminar
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        if (idSeleccionado == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, seleccione un empleado de la tabla para eliminar.");
        return; //Verifica que el usuario hay seleccionado un registro
    }

    //Preguntar de forma segura si realmente quiere borrarlo
    int respuesta = javax.swing.JOptionPane.showConfirmDialog(this, 
            "¿Está seguro de que desea eliminar este registro?", 
            "Confirmar Eliminación", 
            javax.swing.JOptionPane.YES_NO_OPTION, 
            javax.swing.JOptionPane.WARNING_MESSAGE);

    //Si el usuario presiona Sí, procedemos a borrar
    if (respuesta == javax.swing.JOptionPane.YES_OPTION) {
        
        //Instanciamos
        EmpleadoModel empModel = new EmpleadoModel();
        
        //Llamamos al método "eliminar" de nuestra clase EmpleadoModel
        //pasándole el ID guardado
        if (empModel.eliminar(idSeleccionado)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Empleado eliminado correctamente.");
            
            //Refrescamos la tabla
            cargarDatosEnTabla();
            
            //Reiniciamos el ID y limpiamos las cajas de texto
            idSeleccionado = -1;
            txtNombre.setText("");
            txtSalario.setText("");
            cmbDepartamento.setSelectedIndex(0); // Vuelve al primer elemento del combobox
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Hubo un error al intentar eliminar el registro.");
        }
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

    //Boton Buscar
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
    //Captura el texto que el usuario quiere buscar
    String textoBuscado = txtBuscarporNombre.getText().trim();
    
    if (textoBuscado.isEmpty()) {
        // Si no escribió nada, asumimos que quiere ver toda la tabla de nuevo
        cargarDatosEnTabla(); 
        javax.swing.JOptionPane.showMessageDialog(this, "Escriba un nombre para buscar.", "Campo vacío", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    //Obtenemos el modelo visual de la tabla y la vaciamos
    javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) JTablaEmpleados.getModel();
    modelo.setRowCount(0); 
    
    //Llamar al metodo "buscarPorNombre" de nuestra clase EmpleadoModel
    //y obtenemos la lista filtrada
    EmpleadoModel empModel = new EmpleadoModel();
    List<Empleado> listaFiltrada = empModel.buscarPorNombre(textoBuscado);
    
    //Se llena la tabla con los resultados encontrados
    for (Empleado emp : listaFiltrada) {
        Object[] fila = new Object[6];
        fila[0] = emp.getId();
        fila[1] = emp.getNombre();
        fila[2] = emp.getDepartamento();
        fila[3] = emp.getSalario();
        fila[4] = emp.getBono();
        fila[5] = emp.getSalarioTotal();
        
        modelo.addRow(fila);
    }
    
    //Avisamos al usuario si no se encontró a nadie
    if (listaFiltrada.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "No se encontraron empleados con ese nombre.");
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

    
