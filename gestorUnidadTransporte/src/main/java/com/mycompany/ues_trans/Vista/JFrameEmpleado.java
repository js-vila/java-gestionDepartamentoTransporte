    //Boton Generar PDF
    private void btnPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPDFActionPerformed
    
    //Aca construimos nuestro reporte pdf
    //El reporte debe de mostrar el costo total por departamento
    //Y al final tambien muestra la suma total de ambos
    JFileChooser selector = new JFileChooser();
    selector.setDialogTitle("Guardar Reporte de Planilla");
    selector.setSelectedFile(new File("Reporte_Planilla.pdf"));

    String rutaElegida = "";
    boolean nombreValido = false;

    while (!nombreValido) {
        int respuesta = selector.showSaveDialog(this);

        // Si el usuario presiona "Guardar"
        if (respuesta == JFileChooser.APPROVE_OPTION) {
            rutaElegida = selector.getSelectedFile().getAbsolutePath();
            
            if (!rutaElegida.endsWith(".pdf")) {
                rutaElegida += ".pdf";
            }
            //Creamos un objeto File para verificar si físicamente ya existe en el disco
            File archivoExistente = new File(rutaElegida);

            if (archivoExistente.exists()) {
                //Si ya existe, lanzamos una advertencia al usuario para que decida sobrrescribir o no
                int confirmar = javax.swing.JOptionPane.showConfirmDialog(
                        this,
                        "El archivo ya existe en esta ubicación.\n¿Desea reemplazarlo?",
                        "Confirmar Sobreescritura",
                        javax.swing.JOptionPane.YES_NO_OPTION,
                        javax.swing.JOptionPane.WARNING_MESSAGE
                );

                if (confirmar == javax.swing.JOptionPane.YES_OPTION) {
                    nombreValido = true; // El usuario acepto sobreescribir el archivo
                }
            } else {
                nombreValido = true; // El archivo no existe, la ruta está libre y es segura
            }
            
        } else {
            return; 
        }
    }
    
    //Instanciamos el documento 
    Document documento = new Document();
    
    try {
        //El usuario define la ruta donde se guardará
        PdfWriter.getInstance(documento, new FileOutputStream(rutaElegida));
        
        //Abrimos el documento para empezar a editar sus carateristicas
        documento.open();
        
        //Le damos estilo a los diferentes titulos y subtitulos  
        Font fuenteTitulo = FontFactory.getFont(FontFactory.HELVETICA_BOLD, 18, BaseColor.DARK_GRAY);
        Font fuenteSubtitulo = FontFactory.getFont(FontFactory.HELVETICA, 12, BaseColor.GRAY);
        Font fuenteCabecera = FontFactory.getFont(FontFactory.HELVETICA_BOLD, 12, BaseColor.WHITE);
        
  
        //Este es el titulo y aparecera hasta arriba del documento
        Paragraph titulo = new Paragraph("Reporte de Costos de Planilla\n", fuenteTitulo);
        titulo.setAlignment(1); // Centrado
        documento.add(titulo);
        
        Paragraph subtitulo = new Paragraph("UES-Trans | San Salvador, El Salvador\n\n", fuenteSubtitulo);
        subtitulo.setAlignment(1);
        documento.add(subtitulo);
        
        //Definimos el estilo de la tabla
        PdfPTable tablaPdf = new PdfPTable(2);
        tablaPdf.setWidthPercentage(80); 
        
        //Creamos celdas personalizadas para la cabecera
        PdfPCell celda1 = new PdfPCell(new Phrase("Departamento", fuenteCabecera));
        celda1.setBackgroundColor(new BaseColor(0, 102, 204)); 
        celda1.setHorizontalAlignment(1);
        celda1.setPadding(8); 
        tablaPdf.addCell(celda1);
        
        PdfPCell celda2 = new PdfPCell(new Phrase("Costo Total de Planilla ($)", fuenteCabecera));
        celda2.setBackgroundColor(new BaseColor(0, 102, 204));
        celda2.setHorizontalAlignment(1);
        celda2.setPadding(8);
        tablaPdf.addCell(celda2);
        
        //Lenamos con los datos
        EmpleadoModel empModel = new EmpleadoModel();
        java.util.Map<String, Double> costos = empModel.obtenerCostosPorDepartamento();
        
        double granTotalEmpresa = 0;
        
        for (java.util.Map.Entry<String, Double> entrada : costos.entrySet()) {
            // Creamos celdas normales para alinear el texto de los datos
            PdfPCell celdaDepto = new PdfPCell(new Phrase(entrada.getKey()));
            celdaDepto.setPadding(5);
            tablaPdf.addCell(celdaDepto);
            
            PdfPCell celdaCosto = new PdfPCell(new Phrase(String.format("$ %.2f", entrada.getValue())));
            celdaCosto.setHorizontalAlignment(2);
            celdaCosto.setPadding(5);
            tablaPdf.addCell(celdaCosto);
            
            granTotalEmpresa += entrada.getValue();
        }
        
        documento.add(tablaPdf);
        
        //Destacamos al final el documento el total 
        Font fuenteTotal = FontFactory.getFont(FontFactory.HELVETICA_BOLD, 14, new BaseColor(204, 0, 0)); // Color Rojo
        Paragraph textoTotal = new Paragraph("\nCosto Total General: $" + String.format("%.2f", granTotalEmpresa), fuenteTotal);
        textoTotal.setAlignment(2); 
        documento.add(textoTotal);
     
        
        //Cerramos y guardamos
        documento.close();
        
        //Le mostramos al usuario que todo salió bien
        javax.swing.JOptionPane.showMessageDialog(this, "¡Reporte generado con éxito en tu Escritorio!", "Reporte Listo", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        
    } catch (Exception e) {
        // Capturamos cualquier error (si el archivo está abierto y no se puede sobreescribir, o si falta la librería)
        javax.swing.JOptionPane.showMessageDialog(this, "Error al generar el PDF: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnPDFActionPerformed
    
    
