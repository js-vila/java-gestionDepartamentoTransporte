    //Boton Nuevo
    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed

        //Este bloque de codigo valida que ningun campo este vacio       
        if (txtNombre.getText().trim().isEmpty() || txtSalario.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, no deje campos vacíos.", "Campos incompletos", javax.swing.JOptionPane.WARNING_MESSAGE);
        return; // 
    }
        
        double salarioNuevo = 0;
        try {
        salarioNuevo = Double.parseDouble(txtSalario.getText().trim());
        if (salarioNuevo <= 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "El salario debe ser mayor a cero.", "Dato inválido", javax.swing.JOptionPane.ERROR_MESSAGE);
            return; //Valida que sea mayor que cero
        }
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "El salario debe ser un número válido, sin letras.", "Error de formato", javax.swing.JOptionPane.ERROR_MESSAGE);
        return; // Valida que sean estrictamente numeros
    }
        
        //Captura el nombre del formulario
        String nombre = txtNombre.getText();
        String departamento = cmbDepartamento.getSelectedItem().toString();
        
        //Instancia de la clase empleado
        Empleado empleado = new Empleado(nombre, departamento, salarioNuevo);
        
    //Se elige qué método sobrecargado usar según el departamento
    if (departamento.equals("Administrativo")) {
         empleado.CalcularBono(salarioNuevo); // 1 parámetro
    } else if (departamento.equals("Motorista")) {
         empleado.CalcularBono(salarioNuevo, 30); // 2 parámetros (con viáticos fijos equivalente a 30 dolares)
    }

    //Enviamos el objeto empleado a la base de datos a traves del metodo insertar
    //de la clase EmpleadoModel  
    EmpleadoModel empModel = new EmpleadoModel();
    if (empModel.insertar(empleado)) {
        JOptionPane.showMessageDialog(null, "Empleado guardado con éxito.");
        //Aquí llamamos al método para actualizar el JTable
        cargarDatosEnTabla();       
    } else {
        JOptionPane.showMessageDialog(null, "Error al guardar en BD.");
    }
            //Limpiamos los campos
            txtNombre.setText("");
            cmbDepartamento.getItemAt(0);
            txtSalario.setText("");  
    }//GEN-LAST:event_btnNuevoActionPerformed

